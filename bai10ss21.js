// Ví dụ về if/else
let a = 10;

// Câu lệnh if/else được sử dụng khi thực hiện một hành động dựa trên một điều kiện cụ thể
if (a > 5) {
    console.log("a lớn hơn 5");
} else {
    console.log("a không lớn hơn 5");
}

// Ví dụ về switch/case
let fruit = "apple";

// Câu lệnh switch/case được sử dụng khi thực hiện các hành động khác nhau dựa trên giá trị của một biến hoặc biểu thức
switch (fruit) {
    case "apple":
        console.log("Đây là quả táo");
        break;
    case "banana":
        console.log("Đây là quả chuối");
        break;
    default:
        console.log("Không xác định được loại quả");
}
